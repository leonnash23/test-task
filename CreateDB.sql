CREATE TABLE if NOT EXISTS PUBLIC.CUSTOMERS(ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL PRIMARY KEY,
  NAME VARCHAR(30),
  FNAME VARCHAR(30),
  SNAME VARCHAR(30),
  PHONE BIGINT);

CREATE TABLE if NOT EXISTS PUBLIC.ORDERS(ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL PRIMARY KEY,
  DESCRIPTION VARCHAR(300) NOT NULL,
  CUSTOMER BIGINT NOT NULL,
  START TIMESTAMP NOT NULL,
  ENDDATE TIMESTAMP,
  COST DOUBLE,
  STATUS VARCHAR(30) NOT NULL,
  CONSTRAINT FOREING_KEY_FOR_CUSTOMER FOREIGN KEY(CUSTOMER) REFERENCES PUBLIC.CUSTOMERS(ID));